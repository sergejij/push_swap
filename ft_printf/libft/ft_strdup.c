#include "libft.h"

char	*ft_strdup(const char *src)
{
	size_t	len;
	char	*copy;

	len = ft_strlen(src);
	if (!(copy = (char *)malloc(sizeof(char) * (len + 1))))
		return (NULL);
	ft_strcpy(copy, src);
	return (copy);
}
