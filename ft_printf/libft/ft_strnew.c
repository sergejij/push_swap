#include "libft.h"

char	*ft_strnew(size_t size)
{
	char *temp;

	temp = NULL;
	if (!(size + 1))
		return (NULL);
	temp = (char *)malloc((size + 1) * sizeof(char));
	if (!temp)
		return (NULL);
	ft_memset(temp, '\0', size);
	temp[size] = '\0';
	return (temp);
}
