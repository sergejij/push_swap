#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char	*buff;
	int		i;
	int		j;

	buff = s1;
	i = 0;
	j = 0;
	while (s1[i])
		i++;
	while (s2[j])
	{
		buff[i] = s2[j];
		i++;
		j++;
	}
	buff[i] = '\0';
	return (s1);
}
